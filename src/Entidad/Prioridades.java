package Entidad;

public class Prioridades {
	private String idPrioridades;
	private String prioridades;
	
	 public Prioridades(){
	    	
	 }
	 public Prioridades(String idPrioridades, String prioridades) {
	        this.idPrioridades = idPrioridades;
	        this.prioridades = prioridades;
	 }
	public String getIdPrioridades() {
		return idPrioridades;
	}
	public void setIdPrioridades(String idPrioridades) {
		this.idPrioridades = idPrioridades;
	}
	public String getPrioridades() {
		return prioridades;
	}
	public void setPrioridades(String prioridades) {
		this.prioridades = prioridades;
	}
	

	
	
}
